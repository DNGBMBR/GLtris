package menu.component;

public class TextInfo {
	public String text;
	public float fontSize;
	public float startX, startY;
	public float r, g, b;

	public TextInfo(String text, float fontSize, float startX, float startY, float r, float g, float b) {
		this.text = text;
		this.fontSize = fontSize;
		this.startX = startX;
		this.startY = startY;
		this.r = r;
		this.g = g;
		this.b = b;
	}
}
